/**
 * Get translation for a specific language
 * Falls back to English if language not found
 */
export function getTranslation(lang: string): WeatherTranslations {
  return translations[lang as SupportedLanguage] || translations.en;
}

/**
 * Translate country name based on ISO code or common name
 */
export function translateCountry(country: string, lang: string): string {
  const t = getTranslation(lang);
  
  const countryMap: Record<string, keyof Pick<WeatherTranslations, 
    'unitedStates' | 'spain' | 'france' | 'germany' | 'italy' | 'japan' | 
    'china' | 'russia' | 'brazil' | 'india' | 'unitedKingdom' | 'canada' | 
    'australia' | 'mexico'>> = {
    'United States': 'unitedStates',
    'United States of America': 'unitedStates',
    'USA': 'unitedStates',
    'US': 'unitedStates',
    'Spain': 'spain',
    'France': 'france',
    'Germany': 'germany',
    'Italy': 'italy',
    'Japan': 'japan',
    'China': 'china',
    'Russia': 'russia',
    'Brazil': 'brazil',
    'India': 'india',
    'United Kingdom': 'unitedKingdom',
    'UK': 'unitedKingdom',
    'Canada': 'canada',
    'Australia': 'australia',
    'Mexico': 'mexico',
  };

  const key = countryMap[country];
  return key ? t[key] : country; // Return original if no translation found
}
